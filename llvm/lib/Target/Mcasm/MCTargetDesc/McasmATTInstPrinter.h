// This class prints Mcasm MCInst to mcasm assembly syntax.
//
// MCASM NOTE: This is a minimal rewrite for the mcasm backend. It outputs
// mcasm-specific syntax (no % prefix for registers, [rsp+N] for memory).
//
//===----------------------------------------------------------------------===//

#ifndef LLVM_LIB_TARGET_MCASM_MCTARGETDESC_MCASMATTINSTPRINTER_H
#define LLVM_LIB_TARGET_MCASM_MCTARGETDESC_MCASMATTINSTPRINTER_H

#include "llvm/MC/MCInstPrinter.h"

namespace llvm {

class McasmATTInstPrinter final : public MCInstPrinter {
public:
  McasmATTInstPrinter(const MCAsmInfo &MAI, const MCInstrInfo &MII,
                      const MCRegisterInfo &MRI)
      : MCInstPrinter(MAI, MII, MRI) {}

  void printRegName(raw_ostream &OS, MCRegister Reg) override;
  void printInst(const MCInst *MI, uint64_t Address, StringRef Annot,
                 const MCSubtargetInfo &STI, raw_ostream &OS) override;

  // Autogenerated by tblgen
  std::pair<const char *, uint64_t>
  getMnemonic(const MCInst &MI) const override;
  void printInstruction(const MCInst *MI, uint64_t Address, raw_ostream &OS);
  static const char *getRegisterName(MCRegister Reg);

  void printOperand(const MCInst *MI, unsigned OpNo, raw_ostream &OS);
  void printMemReference(const MCInst *MI, unsigned Op, raw_ostream &OS);

  // Memory size variants (all map to printMemReference for mcasm)
  void printdwordmem(const MCInst *MI, unsigned OpNo, raw_ostream &O) {
    printMemReference(MI, OpNo, O);
  }
};

} // namespace llvm

#endif
